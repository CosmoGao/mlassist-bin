function 步骤3()
	等待空闲()	
	if(取当前地图名()~="艾尔莎岛")then 
		回城()
		if(取当前地图名()~="艾尔莎岛")then 
			日志("必须先定居新城【艾尔莎岛】")
			return
		end
	end
	设置("遇敌全跑",1)
	自动寻路(130, 50, "盖雷布伦森林")	
	自动寻路(246, 76, "路路耶博士的家")	
	自动寻路(7,8)
	设置("遇敌全跑",0)
	对话选是(2)
	等待(2000)
	等待战斗结束()
	等待空闲()	
	自动寻路(3, 10, "盖雷布伦森林")		
	等待(1000)
	任务步骤= 任务步骤+1
	回城()
end

function main()	
	while true do
		if(任务步骤==1)then
			步骤1()
		elseif(任务步骤==2)then
			步骤2()
		elseif(任务步骤==3)then
			步骤3()
		elseif(任务步骤>=4)then
			日志("任务已完成")
			设置("移动速度",100)
			break
		end
	end
end
main()

